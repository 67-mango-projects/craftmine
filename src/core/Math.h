struct Vector2 {
	float x, y;
	Vector2() : x(0), y(0) {}                 // default ctor
	Vector2(float x1, float y1) : x(x1), y(y1) {}
};

struct Vector3 {
	float x, y,z;
	Vector3() : x(0), y(0), z(0) {}                 // default ctor
	Vector3(float x1, float y1,float z1) : x(x1), y(y1),z(z1) {}
};